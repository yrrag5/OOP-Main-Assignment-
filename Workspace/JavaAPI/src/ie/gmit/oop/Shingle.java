package ie.gmit.oop;

public class Shingle {
	private int docId;
	private int hashcode;
	//Constructor + Getters/Setters
	public int getDocId() {
		return docId;
	}
	public void setDocId(int docId) {
		this.docId = docId;
	}
	public int getHashcode() {
		return hashcode;
	}
	public void setHashcode(int hashcode) {
		this.hashcode = hashcode;
	}

}
